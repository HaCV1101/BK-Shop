create database IF NOT EXISTS BKSHOP;

use BKSHOP;

create table IF NOT EXISTS KhachHang(
    IDKhachHang char(15) NOT NULL,
    TenKhachHang nvarchar(50) NOT NULL,
    SDT char(11) NOT NULL,
    DiemTichLuy int NOT NULL,
    primary key (IDKhachHang)
);

create table IF NOT EXISTS NguoiDung(
    IDNguoiDung char(15) NOT NULL,
    TenNguoiDung nvarchar(50) NOT NULL,
    TenDangNhap varchar(50) NOT NULL,
    MatKhau varchar(30) NOT NULL,
    NgaySinh date NOT NULL,
    SDT char(11) NOT NULL,
    DiaChi nvarchar(255) NOT NULL,
    primary key (IDNguoiDung)
);

create table IF NOT EXISTS SanPham(
    IDSanPham char(15) NOT NULL,
    TenSanPham nvarchar(255) NOT NULL,
    GiaBan int NOT NULL,
    SoLuong int NOT NULL,
    ChiTietSP nvarchar(50),
    primary key (IDSanPham)
);

create table IF NOT EXISTS HoaDon(
    IDHoaDon char(15) NOT NULL,
    IDNguoiDung char(15) NOT NULL,
    IDKhachHang char(15),
    PhanTramCK int,
    TruDTL int,
    NgayBan date NOT NULL,
    primary key (IDHoaDon),
    constraint fk_hd1 foreign key (IDNguoiDung) references NguoiDung(IDNguoiDung) on delete cascade on update cascade,
    constraint fk_hd2 foreign key (IDKhachHang) references KhachHang(IDKhachHang) on delete cascade on update cascade
);

create table IF NOT EXISTS ChiTietHD(
    IDHoaDon char(15) NOT NULL,
    IDSanPham char(15) NOT NULL,
    SoLuong int(11) NOT NULL,
    primary key (IDHoaDon, IDSanPham),
    constraint fk_ct1 foreign key (IDHoaDon) references HoaDon(IDHoaDon) on delete cascade on update cascade,
    constraint fk_ct2 foreign key (IDSanPham) references SanPham(IDSanPham) on delete cascade on update cascade
);

